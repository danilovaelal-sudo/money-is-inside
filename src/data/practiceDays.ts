import type { PracticeDay, PracticeQuestion } from "@/types";

function textQuestions(labels: string[]): PracticeQuestion[] {
  return labels.map((label, index) => ({
    id: `q${index + 1}`,
    label,
    type: "textarea",
    required: true
  }));
}

function creativeFields(labels: string[]): PracticeQuestion[] {
  return labels.map((label, index) => ({
    id: `c${index + 1}`,
    label,
    type: "textarea"
  }));
}

